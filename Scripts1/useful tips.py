
#Continuation character |
number1 = 1
number2 = 2
number3 =3
summation = number1 + number2 + number3
summation2 = number1 +\
             number2 +\
             number3

print(summation)
print(summation2)

#multiple statements using ;
print(1); print(2); print(3);

#multiple assignment

x, y, z = 1, "Hello", 5.1
print(x)
print(y)
print(z)

sample = 1, "Hello", 5.1
x, y, z = sample
print(x)
print(y)
print(z)

#swapping 
x, z = 1, 5.1
x, z = z, x
print(x)
print(z)

#chained assignments
show = print
display = print
show("show")
display("display")
print("print")

#we can also write it as:
show = display = print
show("show")
display("display")
print("print")

first = second = "chained assignment"
print(first)
print(second)

display = print


     
